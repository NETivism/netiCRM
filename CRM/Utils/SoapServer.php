  }

  /**
   * Handle a mailing reply event via SOAP.
   *
   * @param string      $key       The SOAP authentication key.
   * @param int         $job       The mailing job ID.
   * @param int         $queue     The event queue ID.
   * @param string      $hash      The verification hash.
   * @param string      $bodyTxt   The plain text reply body.
   * @param string      $rt        The reply-to address.
   * @param string|null $bodyHTML   The HTML reply body, or NULL.
   * @param string|null $fullEmail The full email content, or NULL.
   *
   * @return mixed The result from civicrm_mailer_event_reply().
   */
  public function mailer_event_reply($key, $job, $queue, $hash, $bodyTxt, $rt, $bodyHTML = NULL, $fullEmail = NULL) {
    $this->verify($key);
    $params = ['job_id' => $job,
      'event_queue_id' => $queue,
      'hash' => $hash,
      'bodyTxt' => $bodyTxt,
      'replyTo' => $rt,
      'bodyHTML' => $bodyHTML,
      'fullEmail' => $fullEmail,
    ];
    return civicrm_mailer_event_reply($params);
  }

  /**
   * Handle a mailing forward event via SOAP.
   *
   * @param string $key   The SOAP authentication key.
   * @param int    $job   The mailing job ID.
   * @param int    $queue The event queue ID.
   * @param string $hash  The verification hash.
   * @param string $email The email address to forward to.
   *
   * @return mixed The result from civicrm_mailer_event_forward().
