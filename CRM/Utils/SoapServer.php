/**
 * This class handles all SOAP client requests.
 *
 * @copyright CiviCRM LLC (c) 2004-2010
 *
 */

class CRM_Utils_SoapServer {

  /**
   * Number of seconds we should let a SOAP process idle before expiring the key.
   *
   * A value of 0 means no timeout.
   *
   * @var int
   */
  public static $soap_timeout = 0;

  /**
   * The cached user framework class name used for authentication.
   *
   * @var string
   */
  public $ufClass;

  /**
   * Class constructor.
   *
   * Caches the user framework class locally so it can be used
   * for authentication and validation.
   *
   * @param string $uf The user framework class name.
   */
  public function __construct() {
    // any external program which call SoapServer is responsible for
    // creating and attaching the session
    $args = func_get_args();
    $this->ufClass = array_shift($args);
  }

  /**
   * Simple ping function to test for liveness.
   *
   * @param string $var The string to be echoed back.
   *
   * @return string The PONG response containing the input and the previous key.
   */
  public function ping($var) {
    $session = CRM_Core_Session::singleton();
    $key = $session->get('key');
    $session->set('key', $var);
    return "PONG: $var ($key)";
  }

  /**
   * Verify a SOAP key.
   *
   * Checks that the provided key matches the stored SOAP key and has not expired.
   *
   * @param string $key The SHA1-hashed SOAP key generated by authenticate().
   *
   * @return void
   *
   * @throws \SoapFault If the key is invalid or expired.
   */
  public function verify($key) {
    $session = CRM_Core_Session::singleton();

    $soap_key = $session->get('soap_key');
    $t = time();

    if ($key !== sha1($soap_key)) {
      throw new SoapFault('Client', 'Invalid key');
    }

    if (self::$soap_timeout &&
      $t > ($session->get('soap_time') + self::$soap_timeout)
    ) {
      throw new SoapFault('Client', 'Expired key');
    }

    /* otherwise, we're ok.  update the timestamp */

    $session->set('soap_time', $t);
  }

  /**
   * Authenticate a user via the user framework class.
   *
   * Delegates authentication to the UF class and stores the SOAP key
   * and timestamp in the session upon success.
   *
   * @param string $name The login name.
   * @param string $pass The password.
   *
   * @return string The SHA1-hashed SOAP client key.
   *
   * @throws \SoapFault If the login credentials are invalid.
   */
  public function authenticate($name, $pass) {
    $ufClassName = $this->ufClass;
    $result = &$ufClassName::authenticate($name, $pass);

    if (empty($result)) {
      throw new SoapFault('Client', 'Invalid login');
    }

    $session = CRM_Core_Session::singleton();
    $session->set('soap_key', $result[2]);
    $session->set('soap_time', time());

    return sha1($result[2]);
  }

  /**
   * Handle a mailing bounce event via SOAP.
   *
   * @param string $key  The SOAP authentication key.
   * @param int    $job  The mailing job ID.
   * @param int    $queue The event queue ID.
   * @param string $hash The verification hash.
   * @param string $body The bounce message body.
   *
   * @return mixed The result from civicrm_mailer_event_bounce().
   */
  public function mailer_event_bounce($key, $job, $queue, $hash, $body) {
    $this->verify($key);
    $params = ['job_id' => $job,
      'event_queue_id' => $queue,
      'hash' => $hash,
      'body' => $body,
    ];
    return civicrm_mailer_event_bounce($params);
  }

  /**
   * Handle a mailing unsubscribe event via SOAP.
   *
   * @param string $key  The SOAP authentication key.
