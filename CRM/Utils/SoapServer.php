  /**
   * Handle a mailing subscribe event via SOAP.
   *
   * @param string $key    The SOAP authentication key.
   * @param string $email  The email address to subscribe.
   * @param string $domain The domain (unused in underlying call).
   * @param int    $group  The group ID to subscribe to.
   *
   * @return mixed The result from civicrm_mailer_event_subscribe().
   */
  public function mailer_event_subscribe($key, $email, $domain, $group) {
    $this->verify($key);
    $params = ['email' => $email,
      'group_id' => $group,
    ];
    return civicrm_mailer_event_subscribe($params);
  }

  /**
   * Handle a mailing subscription confirmation event via SOAP.
   *
   * @param string $key       The SOAP authentication key.
   * @param int    $contact   The contact ID.
   * @param int    $subscribe The subscription ID.
   * @param string $hash      The verification hash.
   *
   * @return mixed The result from civicrm_mailer_event_confirm().
   */
  public function mailer_event_confirm($key, $contact, $subscribe, $hash) {
    $this->verify($key);
    $params = ['contact_id' => $contact,
      'subscribe_id' => $subscribe,
      'hash' => $hash,
    ];
