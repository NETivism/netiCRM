  }

  /**
   * Retrieve contact data via SOAP.
   *
   * @param string $key    The SOAP authentication key.
   * @param array  $params The parameters to pass to civicrm_contact_get().
   *
   * @return mixed The result from civicrm_contact_get().
   */
  public function get_contact($key, $params) {
    $this->verify($key);

    return civicrm_contact_get($params);
  }
}
