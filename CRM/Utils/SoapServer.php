      'event_queue_id' => $queue,
      'hash' => $hash,
    ];
    return civicrm_mailer_event_unsubscribe($params);
  }

  /**
   * Handle a domain-level mailing unsubscribe event via SOAP.
   *
   * @param string $key  The SOAP authentication key.
